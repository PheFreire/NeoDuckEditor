--=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-(Split window management)-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-

Km.set("n", "<leader>s", "<C-w>v") -- split window vertically
Km.set("n", "<leader>ss", "<C-w>s") -- split window horizontally
Km.set("n", "<leader>sx", "<cmd>close<CR>") -- close split window
Km.set("n", "<leader>sj", "<C-w>-") -- make split window height shorter
Km.set("n", "<leader>sk", "<C-w>+") -- make split windows height taller
Km.set("n", "<leader>sl", "<C-w>>5") -- make split windows width bigger 
Km.set("n", "<leader>sh", "<C-w><5") -- make split windows width smaller
