--=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-(Tab Management)-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-

Km.set("n", "<leader>to", "<cmd>tabnew<CR>") -- open a new tab
Km.set("n", "<leader>tx", "<cmd>tabclose<CR>") -- close a tab
Km.set("n", "<leader>l", "<cmd>tabn<CR>") -- next tab
Km.set("n", "<leader>h", "<cmd>tabp<CR>") -- previous tab
